#include <iostream>
using namespace std;

int main()
{
    int a = 10;
    int b = 20;
    //==
    cout << (a == b) << endl;
    //!=
    cout << (a != b) << endl;
    //>
    cout << (a > b) << endl;
    //<
    cout << (a < b) << endl;
    //>=
    cout << (a >= b) << endl;
    //<=
    cout << (a <= b) << endl;
    // c语言中比较运算表达式,得到的结果都用1,0表示
}